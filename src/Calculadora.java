public class Calculadora {
    public int sum(int valueA, int valueB) {
        return valueA + valueB;
    }

    public int sub(int valueA, int valueB) {
        return valueA - valueB;
    }

    public int mult(int valueA, int valueB) {
        return valueA * valueB;
    }

    public int div(int valueA, int valueB) {
        return valueA / valueB;
    }
}
